export type PostQuery = { postsCollection?: { items: Array<{ title?: string | null } | null> } | null };


export const PostDocument = gql`
    query post($slug: String) {
  postsCollection(where: {slug: $slug}) {
    items {
      title
    }
  }
}
    `;

